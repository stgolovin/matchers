import sortArr from '../main';

test('simple test', () => {
  const characters = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  const result = sortArr(characters);

  expect(result).toEqual(expected);
  // expect(result).not.toBe(expected);
});
